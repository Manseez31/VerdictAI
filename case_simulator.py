# ---------------------------------------------------------------------------
# Agent 4: Evidence Review
# ---------------------------------------------------------------------------

def evidence_review(
    case: Dict[str, str],
    analysis: Dict[str, Any],
    prosecution: Dict[str, Any],
    defense: Dict[str, Any],
    llm,
) -> Dict[str, Any]:
    prompt = f"""{_SAFETY_PREAMBLE}
ROLE: You are the EVIDENCE REVIEW agent — a neutral forensic/evidentiary expert.
Educational analysis only.

{_case_block(case)}

{_upstream("CASE ANALYSIS", analysis)}

{_upstream("PROSECUTION VIEW", prosecution, 2000)}

{_upstream("DEFENSE VIEW", defense, 2000)}

TASK: For the evidence in this case, evaluate:
- witness reliability
- document quality
- digital evidence quality
- chain-of-custody concerns
Rate each evidence item and the overall evidence strength as Strong, Moderate, or Weak.

Return ONLY this JSON schema:
{{
  "evidence_assessments": [
    {{"evidence": "...", "strength": "Strong|Moderate|Weak", "reasoning": "..."}}
  ],
  "witness_reliability": "assessment (or 'No witnesses described')",
  "document_quality": "assessment (or 'No documents described')",
  "digital_evidence_quality": "assessment (or 'No digital evidence described')",
  "chain_of_custody_concerns": ["concern", ...],
  "overall_strength": "Strong|Moderate|Weak",
  "reasoning_summary": "3-5 sentence summary of the evidentiary picture",
  "laws_referenced": ["evidence-law principle applied", ...],
  "confidence": 0-100
}}"""
    raw = _run_agent(llm, "EvidenceReview", prompt)

    assessments: List[Dict[str, str]] = []
    if isinstance(raw.get("evidence_assessments"), list):
        for item in raw["evidence_assessments"][:20]:
            if not isinstance(item, dict):
                continue
            evidence = _text(item.get("evidence"), 500)
            if not evidence:
                continue
            assessments.append({
                "evidence": evidence,
                "strength": _enum(item.get("strength"), STRENGTHS, "Moderate"),
                "reasoning": _text(item.get("reasoning"), 800),
            })

    return {
        "evidence_assessments": assessments,
        "witness_reliability": _text(raw.get("witness_reliability"), 800),
        "document_quality": _text(raw.get("document_quality"), 800),
        "digital_evidence_quality": _text(raw.get("digital_evidence_quality"), 800),
        "chain_of_custody_concerns": _str_list(raw.get("chain_of_custody_concerns")),
        "overall_strength": _enum(raw.get("overall_strength"), STRENGTHS, "Moderate"),
        "reasoning_summary": _text(raw.get("reasoning_summary")),
        "laws_referenced": _str_list(raw.get("laws_referenced")),
        "confidence": _confidence(raw.get("confidence")),
        **({"parse_error": True} if raw.get("parse_error") else {}),
    }


# ---------------------------------------------------------------------------
# Agent 5: Judge
# ---------------------------------------------------------------------------

def judge_case(
    case: Dict[str, str],
    analysis: Dict[str, Any],
    prosecution: Dict[str, Any],
    defense: Dict[str, Any],
    evidence: Dict[str, Any],
    llm,
) -> Dict[str, Any]:
    prompt = f"""{_SAFETY_PREAMBLE}
ROLE: You are the JUDGE agent — a neutral judicial officer simulating how a court
might reason about this case. Educational analysis only; this is NOT a real
verdict and must weigh the presumption of innocence.

{_case_block(case)}

{_upstream("CASE ANALYSIS", analysis, 2000)}

{_upstream("PROSECUTION VIEW", prosecution, 2500)}

{_upstream("DEFENSE VIEW", defense, 2500)}

{_upstream("EVIDENCE REVIEW", evidence, 2500)}

TASK:
- Weigh the prosecution and defense arguments against the evidence review.
- Produce judicial-style legal reasoning and concrete findings.
- Simulate a verdict tendency: exactly one of "Likely Conviction",
  "Uncertain Outcome", or "Likely Acquittal".

Return ONLY this JSON schema:
{{
  "legal_reasoning": "judicial reasoning paragraph(s)",
  "findings": ["specific finding", ...],
  "verdict": "Likely Conviction|Uncertain Outcome|Likely Acquittal",
  "verdict_reasoning": "short explanation of the verdict tendency",
  "reasoning_summary": "3-5 sentence summary",
  "laws_referenced": ["law or principle relied on", ...],
  "confidence": 0-100
}}"""
    raw = _run_agent(llm, "Judge", prompt)
    return {
        "legal_reasoning": _text(raw.get("legal_reasoning"), 6000),
        "findings": _str_list(raw.get("findings")),
        "verdict": _enum(raw.get("verdict"), VERDICTS, "Uncertain Outcome"),
        "verdict_reasoning": _text(raw.get("verdict_reasoning"), 2000),
        "reasoning_summary": _text(raw.get("reasoning_summary")),
        "laws_referenced": _str_list(raw.get("laws_referenced")),
        "confidence": _confidence(raw.get("confidence")),
        **({"parse_error": True} if raw.get("parse_error") else {}),
    }


# ---------------------------------------------------------------------------
# Orchestrator
# ---------------------------------------------------------------------------

def run_case_simulation(case: Dict[str, str], llm=None) -> Dict[str, Any]:
    """Run the full 5-agent workflow and return the final case report.

    `case` must contain: title, description, jurisdiction, case_type.
    Each stage feeds the next, per the architecture:
    Analyzer -> Prosecution -> Defense -> Evidence Review -> Judge.
    """
    llm = llm or _get_default_llm()

    logger.info("Case simulation started: %r", case.get("title"))
    analysis = analyze_case(case, llm)
    prosecution = prosecution_view(case, analysis, llm)
    defense = defense_view(case, analysis, prosecution, llm)
    evidence = evidence_review(case, analysis, prosecution, defense, llm)
    judge = judge_case(case, analysis, prosecution, defense, evidence, llm)
    logger.info("Case simulation finished: verdict=%s", judge.get("verdict"))

    return {
        "case": {
            "title": case["title"],
            "description": case["description"],
            "jurisdiction": case["jurisdiction"],
            "case_type": case["case_type"],
        },
        "analysis": analysis,
        "prosecution": prosecution,
        "defense": defense,
        "evidence_review": evidence,
        "judge": judge,
        "disclaimer": DISCLAIMER,
        "generated_at": datetime.datetime.now().isoformat(timespec="seconds"),
        "model": getattr(llm, "model_name", "unknown"),
    }
